package RelationalOp;

public class First {

    public static void main(String[] args) {

        int x=45;
        int y=90;

        System.out.println(x==y);
        System.out.println(x<y);
        System.out.println(x<=y);
        System.out.println(x>y);
        System.out.println(x>=y);
        System.out.println(!(x<y));


    }
}
