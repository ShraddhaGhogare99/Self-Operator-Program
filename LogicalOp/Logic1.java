package LogicalOp;

public class Logic1 {

    public static void main(String[] args) {

        int num1=90;
        int num2=70;

        System.out.println((num2<num1)&&(num2>num1)); // when both conditions are true then output will be true

        System.out.println((num2<num1)||(num2>num1));  // if at least one condition is true then output will be true


    }

}

